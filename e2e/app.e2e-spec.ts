import { AngularTestPage } from './app.po';
import { ExpectedConditions, browser, element, by } from 'protractor';
import {} from 'jasmine';


describe('Starting tests for angular-app', function() {
  let page: AngularTestPage;

  beforeEach(() => {
    page = new AngularTestPage();
  });

  it('website title should be angular-app', () => {
    page.navigateTo('/');
    return browser.getTitle().then((result)=>{
      expect(result).toBe('angular-app');
    })
  });

  it('network-name should be import_network@0.1.7',() => {
    element(by.css('.network-name')).getWebElement()
    .then((webElement) => {
      return webElement.getText();
    })
    .then((txt) => {
      expect(txt).toBe('import_network@0.1.7.bna');
    });
  });

  it('navbar-brand should be angular-app',() => {
    element(by.css('.navbar-brand')).getWebElement()
    .then((webElement) => {
      return webElement.getText();
    })
    .then((txt) => {
      expect(txt).toBe('angular-app');
    });
  });

  
    it('PurchaseOrder component should be loadable',() => {
      page.navigateTo('/PurchaseOrder');
      browser.findElement(by.id('assetName'))
      .then((assetName) => {
        return assetName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('PurchaseOrder');
      });
    });

    it('PurchaseOrder table should have 19 columns',() => {
      page.navigateTo('/PurchaseOrder');
      element.all(by.css('.thead-cols th')).then(function(arr) {
        expect(arr.length).toEqual(19); // Addition of 1 for 'Action' column
      });
    });
  

  
    it('OEM component should be loadable',() => {
      page.navigateTo('/OEM');
      browser.findElement(by.id('participantName'))
      .then((participantName) => {
        return participantName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('OEM');
      });
    });

    it('OEM table should have 5 columns',() => {
      page.navigateTo('/OEM');
      element.all(by.css('.thead-cols th')).then(function(arr) {
        expect(arr.length).toEqual(5); // Addition of 1 for 'Action' column
      });
    });
  
    it('Supplier component should be loadable',() => {
      page.navigateTo('/Supplier');
      browser.findElement(by.id('participantName'))
      .then((participantName) => {
        return participantName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('Supplier');
      });
    });

    it('Supplier table should have 5 columns',() => {
      page.navigateTo('/Supplier');
      element.all(by.css('.thead-cols th')).then(function(arr) {
        expect(arr.length).toEqual(5); // Addition of 1 for 'Action' column
      });
    });
  

  
    it('Accept component should be loadable',() => {
      page.navigateTo('/Accept');
      browser.findElement(by.id('transactionName'))
      .then((transactionName) => {
        return transactionName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('Accept');
      });
    });
  
    it('Reject component should be loadable',() => {
      page.navigateTo('/Reject');
      browser.findElement(by.id('transactionName'))
      .then((transactionName) => {
        return transactionName.getText();
      })
      .then((txt) => {
        expect(txt).toBe('Reject');
      });
    });
  

});